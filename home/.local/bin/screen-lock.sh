SESSION_TYPE=$XDG_SESSION_TYPE


case "$SESSION_TYPE" in
    "wayland")
        if [[ "$1" == "--suspend" ]]; then
            systemctl suspend
        fi
        hyprlock
        ;;
    "x11")
        if [[ "$1" == "--suspend" ]]; then
            systemctl suspend
        fi
        betterlockscreen -l dim
        ;;
    *)
        echo "The session type is not defined or is not Wayland/X11."
esac
